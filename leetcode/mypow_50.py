class Solution(object):
    d = {}
    def myPow(self, x, n):
        """
        :type x: float
        :type n: int
        :rtype: float
        """
        if not n:
            return 1
        if n < 0:
            return 1 / self.myPow(x, -n)
        if n % 2:
            return x * self.myPow(x, n-1)
        Solution.d[n] = self.myPow(x, n/2)
        return Solution.d[n] * Solution.d[n]
        
